
public class narrowingcasting {

	public static void main(String[] args) {
		double d = 200.06;
		long l = (long)d;
		int i = (int)l;
		
		
		System.out.println("Double Data type value "+d);
		System.out.println("long Data type value "+l);
		System.out.println("int Data type value "+i);

		
		
	}

}
